<?php
function dbconnect() {
    try {
        $bdd = new PDO('mysql:host=localhost;dbname=utilisateurs_db;charset=utf8', 'root', '', [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC
        ]);
        return $bdd;
    } catch (PDOException $e) {
        die("Erreur de connexion : " . $e->getMessage());
    }
}

$bdd = dbconnect();

$error = false; // Initialiser à false

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $password = $_POST['password'];
    $user = $_POST['user'];

    if (!empty($password) && !empty($user)) {
        $stmt = $bdd->prepare("SELECT * FROM utilisateurs WHERE Pseudo = :user AND MDP = :password");
        $stmt->bindParam(':user', $user);
        $stmt->bindParam(':password', $password);
        $stmt->execute();
        $rep = $stmt->fetch();

        if ($rep) {
            // Connexion réussie -> Redirection
            header("Location: ../AdminPanel/dashboard.php#home");
            exit();
        } else {
            $error = true;
        }
    }
}
